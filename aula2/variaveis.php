<h2>Variaveis</h2>
<p>São estruturas (memória) utilizada
     para armazenar dados que serão processados
      pelo programa
</p>

<p> Para criar uma variável PHP basta colocar
    simbolo de $ na frente de um nome e atribuir
    um valor com o simbolo de igual. Exemplo:

    <pre>
     <$variável = 10;
        $nome = "Maria";
        $nota = 5.5;
    </pre>    
</p>

<?php
    $nome = "Luís Guilherme";
    echo "Olá $nome, seja bem vindo a aula de backend";
?>
